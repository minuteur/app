<template>
    <Layout title="Projects">
        <template #header-left>
            <router-link to="/" class="text-white">
                <svg class="inline" width="20" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                    <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M15 19l-7-7 7-7" />
                </svg>
            </router-link>
        </template>

        <template #header-center>
            <div class="text-center">
                <h4 class="mb-1 leading-none text-xs text-gray-200">Client</h4>
                <h3 class="mb-0 leading-none text-lg">Projects</h3>
            </div>
        </template>

        <template #default>
            <div class="flex flex-col h-screen-85vh">
                <div class="flex-grow">
                    <ul v-if="projects.length > 0">
                        <li v-for="(project, index) in projects" :key="project.uuid">
                            <ProjectRow
                                :project="project"
                                :odd="index % 2 === 0"
                                @project:updated="(name) => onProjectUpdated(index, name)"
                            ></ProjectRow>
                        </li>
                    </ul>

                    <div v-else class="h-full flex flex-col justify-center self-center">
                        <div class="text-center">
                            <p class="mb-6 text-gray-800">No projects yet..</p>
                            <img src="./../../assets/empty.svg" alt="No clients found" class="inline">
                        </div>
                    </div>
                </div>

                <footer class="flex justify-between px-6 py-2 bg-gray-800 text-white">
                    <button type="button" @click="create" title="Add new project">+</button>

                    <div class="px-4 py-1 text-xs text-white text-center rounded-md flex items-center">
                        <span class="mr-2">Total time:</span>
                        <Timer
                            v-if="totalTimeSpent > 0"
                            :time="totalTimeSpent"
                            :is-running="false"
                            title="Total timer so far"
                        />
                    </div>
                </footer>
            </div>
        </template>
    </Layout>
</template>

<script>
import Layout from '@components/Layout';
import ProjectRow from '@components/ProjectRow';
import Project from '@models/Project';
import Session from '@models/Session';
import EventManager from '@lib/EventManager';
import Timer from '@components/Timer';

export default {
    name: 'Projects',

    components: {
        Layout,
        ProjectRow,
        Timer
    },

    async mounted () {
        await this.fetchProjects();
        await this.fetchTotalTimeSpent();

        EventManager.listen('sessions.changed', async () => {
            console.log('[Projects.vue] Sessions updated via API, re-fetching to make sure the app is up-to-date');
            this.projects = [];

            await this.fetchProjects();
            await this.fetchTotalTimeSpent();
        });
    },

    beforeDestroy () {
        EventManager.clear('sessions.changed');
    },

    data () {
        return {
            projects: [],
            totalTimeSpent: 0
        };
    },

    methods: {
        async create () {
            let project = await Project.create({
                client_uuid: this.$route.params.uuid,
                name: 'New project'
            });

            this.projects.push(project);
        },

        async fetchProjects () {
            this.projects = await Project.allFromClient(this.$route.params.uuid);
        },

        async fetchTotalTimeSpent () {
            const totalTimeSpent = await Session.totalTimeSpend(this.projects.map(project => project.uuid));
            this.totalTimeSpent = totalTimeSpent.total;
        },

        async onProjectUpdated (index, name) {
            this.projects[index].name = name;

            Project.update(
                this.projects[index].uuid,
                { name: name }
            );
        },
    }
}
</script>
